function writePage(page) {
  const outputPath = join(rootDir, page.path);
  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, renderPage(page), "utf8");
}

function renderPage(page) {
  return `<!doctype html>
<!-- ${marker} -->
<html lang="en">
  <head>
${indent(renderHeadTags(page), 4)}
  </head>
  <body>
    ${renderHeader(page.nav || defaultNav)}
    <main>
${indent(page.body.trim(), 6)}
    </main>
    ${renderFooter()}
${page.script ? `    ${page.script}\n` : ""}  </body>
</html>
`;
}

function renderHeadTags(page) {
  return [
    `<meta charset="utf-8">`,
    `<meta name="viewport" content="width=device-width, initial-scale=1">`,
    `<title>${page.title}</title>`,
    `<meta name="description" content="${page.description}">`,
    `<link rel="canonical" href="${page.canonical}">`,
    page.ogTitle && `<meta property="og:title" content="${page.ogTitle}">`,
    page.ogDescription && `<meta property="og:description" content="${page.ogDescription}">`,
    page.ogType && `<meta property="og:type" content="${page.ogType}">`,
    page.ogUrl && `<meta property="og:url" content="${page.ogUrl}">`,
    page.ogImage && `<meta property="og:image" content="${page.ogImage}">`,
    page.twitterCard && `<meta name="twitter:card" content="${page.twitterCard}">`,
    page.twitterTitle && `<meta name="twitter:title" content="${page.twitterTitle}">`,
    page.twitterDescription && `<meta name="twitter:description" content="${page.twitterDescription}">`,
    `<meta name="theme-color" content="#ef4056">`,
    `<link rel="icon" href="/assets/brand/postsnail-icon.svg" type="image/svg+xml">`,
    `<link rel="alternate icon" href="/assets/brand/postsnail-icon.png">`,
    page.appleTouchIcon && `<link rel="apple-touch-icon" href="/assets/brand/postsnail-icon-192.png">`,
    `<link rel="stylesheet" href="/site.css">`,
  ].filter(Boolean).join("\n");
}

function renderHeader(nav) {
  return `<header class="site-header">
      <a class="brand-link" href="/" aria-label="PostSnail home">
        <img src="${brandIcon}" alt="" width="42" height="42" decoding="async">
        <span>PostSnail</span>
      </a>
      <nav class="site-nav" aria-label="Primary">
${nav.map(([label, href]) => `        <a href="${href}">${label}</a>`).join("\n")}
      </nav>
    </header>`;
}

function renderFooter() {
  return `<footer class="site-footer">
      <img src="${brandIcon}" alt="" width="44" height="44" loading="lazy" decoding="async">
      <p class="legal-line">
        © 2026 Boaz Alhadeff. PostSnail is <a href="/LICENSE">Apache-2.0</a> licensed; redistributed copies must preserve <a href="/NOTICE">NOTICE</a> attribution.
        See <a href="/docs/legal/">Legal</a> and <a href="/THIRD_PARTY_NOTICES.md">Third-party notices</a>.
      </p>
    </footer>`;
}

function docPage(navLabel, title, lede, sections) {
  return `
    <section class="page-hero">
      <p class="eyebrow">Guide</p>
      <h1>${title}</h1>
      <p class="lede">${lede}</p>
    </section>
    <article class="doc-layout">
      <nav class="doc-nav" aria-label="${navLabel}">${sections.map(([id, heading]) => `<a href="#${id}">${heading.replace(/^\d+\.\s*/u, "")}</a>`).join("")}</nav>
      <div class="doc-content">
        ${sections.map(([id, heading, copy]) => `<section id="${id}"><h2>${heading}</h2><p>${copy}</p></section>`).join("\n        ")}
      </div>
    </article>
  `;
}

function indent(value, spaces) {
  const prefix = " ".repeat(spaces);
  return value.split("\n").map((line) => `${prefix}${line}`).join("\n");
}

